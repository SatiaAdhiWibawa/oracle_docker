-- ORACEL DUAL TABLE = biasanya digunakan untuk mengefaluasi, expresi atau fungsi
-- ORECEL harus menggunakan SELECT dengan pasangannya FROM wajib ada

SELECT * FROM DUAL;

-- output : x

-- dalam ORACEL DUAL adalah table khusus yang hanya memiliki satu baris dan satu kolom bernama DUMMY

SELECT SYSDATE FROM DUAL;

-- output : 2021-07-07

SELECT UPPER('hello') FROM DUAL;

-- output : HELLO

SELECT (10 + 10) FROM DUAL;

-- output : 20